import UIKit

extension UIColor {
    
    class var blackColor: UIColor {
        return UIColor.init(named: "black_color")!
    }
    
    class var whiteColor: UIColor {
        return UIColor.init(named: "white_color")!
    }
    
    class var darkGrayColor: UIColor {
        return UIColor.init(named: "dark_gray_color")!
        
    }
}
